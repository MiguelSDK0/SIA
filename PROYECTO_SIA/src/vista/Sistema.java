    public void ListarClientes(){
        List<Cliente> ListarCl = client.ListarClientes();
        modelo = (DefaultTableModel) TablaCliente.getModel();
        Object[] obj = new Object[5];
        for (int i = 0; i < ListarCl.size(); i++) {
            obj[0] = ListarCl.get(i).getId_cliente();
            obj[1] = ListarCl.get(i).getNombre();
            obj[2] = ListarCl.get(i).getDireccion();
            obj[3] = ListarCl.get(i).getTelefono();
            obj[4] = ListarCl.get(i).getEmail();
            modelo.addRow(obj);
        }
        TablaCliente.setModel(modelo);
    }
    
    public void ListarDeudas(){
        List<Deuda> ListarDd = deud.ListarDeuda();
        modelo = (DefaultTableModel) TablaDeudas.getModel();
        Object[] obj = new Object[3];
        for (int i = 0; i < ListarDd.size(); i++) {
            obj[0] = ListarDd.get(i).getId_deuda();
            obj[1] = ListarDd.get(i).getId_cliente();
            obj[2] = ListarDd.get(i).getMonto();
            modelo.addRow(obj);
        }
    }
    
    public void ListarRecibos(){
        List<Recibo> ListarRB = recib.ListarRecibos();
        modelo = (DefaultTableModel) TablaRecibos.getModel();
        Object[] obj = new Object[8];
        for (int i = 0; i < ListarRB.size(); i++) {
            obj[0] = ListarRB.get(i).getCodRecibo();
            obj[1] = ListarRB.get(i).getCodDeuda();
            obj[2] = ListarRB.get(i).getCodCliente();
            obj[3] = ListarRB.get(i).getCodDocumento();
            obj[4] = ListarRB.get(i).getCodTipoPago();
            obj[5] = ListarRB.get(i).getFecha();
            obj[6] = ListarRB.get(i).getFechaExp();
            obj[7] = ListarRB.get(i).getMonto();
            modelo.addRow(obj);
        }
    }
    
    public void LimpiarTabla(){
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

