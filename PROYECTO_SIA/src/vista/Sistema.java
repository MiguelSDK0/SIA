    private void btnGuardarReciboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarReciboActionPerformed
        // TODO add your handling code here:
        int documento = 0;
        int metodo = 0;
        if(cmbDocumentoRecibo.getSelectedItem().equals("Servicio")){
            documento = 1;
        }else if(cmbDocumentoRecibo.getSelectedItem().equals("Producto")){
            documento = 2;
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un tipo de documento");
        }
        
        if(cmbMetodos.getSelectedItem().equals("Efectivo")){
            metodo = 1;
        }else if(cmbMetodos.getSelectedItem().equals("Tarjeta")){
            metodo = 2;
        }else if(cmbMetodos.getSelectedItem().equals("Yape")){
            metodo = 3;
        }else{
            JOptionPane.showMessageDialog(null, "Seleccuibe un metodo de pago");
        }
        
        if(cmbCClienteRecibo.getSelectedItem() != null && cmbDeudaRecibo.getSelectedItem() != null && !"".equals(txtDeudaRecibo.getText()) && !"".equals(txtDateRecibo.getText()) && cmbDocumentoRecibo.getSelectedItem() != null && !"".equals(txtPagarRecibo.getText()) && !"".equals(txtMontoRestanteRecibo.getText()) && !"".equals(txtFechaExpRecibo.getText()) && cmbMetodos.getSelectedItem() != null){
            rb.setCodDeuda(Integer.parseInt(cmbDeudaRecibo.getSelectedItem().toString()));
            rb.setCodCliente(Integer.parseInt(cmbCClienteRecibo.getSelectedItem().toString()));
            rb.setCodDocumento(documento);
            rb.setCodTipoPago(metodo);
            rb.setFecha(txtDateRecibo.getText());
            rb.setFechaExp(txtFechaExpRecibo.getText());
            rb.setMonto(Double.parseDouble(txtPagarRecibo.getText()));
            recib.RegistrarRecibo(rb);
            JOptionPane.showMessageDialog(null,"Recibo registrado");
        }else{
            JOptionPane.showMessageDialog(null, "Hay campos vacios");
        }
    }//GEN-LAST:event_btnGuardarReciboActionPerformed

